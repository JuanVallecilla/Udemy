const container = document.querySelector("#container");
const baseURL = "https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/";
const wikiBaseUrl = "https://pokemon.fandom.com/wiki/";

for (let i = 1; i < listPokemons.length; i++) {
  const pokemon = document.createElement("div");
  pokemon.classList.add("pokemon");
  const newImg = document.createElement("img");
  newImg.src = `${baseURL}${i}.png`;
  const labelNumber = document.createElement("span");
  labelNumber.innerText = `#${i}`;
  const labelName = document.createElement("span");
  labelName.innerHTML = `<a href="${wikiBaseUrl}${listPokemons[i - 1]}">${listPokemons[i - 1]}</a>`;

  pokemon.appendChild(labelNumber);
  pokemon.appendChild(newImg);
  pokemon.appendChild(labelName);
  container.appendChild(pokemon);
}
